#include "../inc/libft.h"

void	ft_striteri(char *s, void (*f)(uint32_t, char*))
{
	uint32_t	i;

	i = 0;
	if (!s || !f)
		return ;
	while (s[i])
	{
		f(i, &s[i]);
		i++;
	}
}
